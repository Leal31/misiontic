# Programa que detecta que un numero esta entre un intervalo de 10 y de 20
# Ingresamos cualquier numero
numero = int(input('Ingresa cualquier numero: '))
# Si el numero es mayor o igual a 10 y es menor o igual a 20 es una condicion verdadera
if numero >= 10 and numero <= 20:
    print('El numero se encuentra entre 10 y entre 20')
else:
    print('El numero no se encuentra entre 10 y entre 20')
